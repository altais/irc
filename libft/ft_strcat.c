#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*s3;
	int		i;

	i = 0;
	s3 = s1 + ft_strlen(s1);
	while (s2[i] != 0)
	{
		s3[i] = s2[i];
		i++;
	}
	s3[i] = 0;
	return (s1);
}
